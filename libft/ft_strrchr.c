#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	const char	*last;

	last = NULL;
	while (*s)
	{
		if (*s++ == (unsigned char)c)
			last = s - 1;
	}
	if (!c)
		return ((char*)s);
	return ((char*)last);
}
