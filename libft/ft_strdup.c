#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	char	*dup;

	if ((dup = malloc(ft_strlen(s1) + 1)) != NULL)
		ft_strcpy(dup, s1);
	return (dup);
}
